// Utility function to get a specific theme value
export function getThemeValue(path: string): any {
  const parts = path.split('.');
  let value: any = theme;
  
  for (const part of parts) {
    if (value && typeof value === 'object' && part in value) {
      value = value[part];
    } else {
      return undefined;
    }
  }
  
  return value;
}

